// @lc code=start
/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution
{
public:
    vector<vector<int>> levelOrder(TreeNode *root)
    {
        vector<vector<int>> res;
        queue<TreeNode *> q;
        if (root == nullptr)
        {
            return res;
        }
        q.emplace(root);
        while (!q.empty())
        {
            int s = q.size();
            vector<int> res_tmp;
            while (s--)
            {
                auto tmp = q.front();
                q.pop();
                if (tmp->left)
                    q.emplace(tmp->left);
                if (tmp->right)
                    q.emplace(tmp->right);
                res_tmp.emplace_back(tmp->val);
            }
            res.emplace_back(res_tmp);
        }
        return res;
    }
};
// @lc code=end
